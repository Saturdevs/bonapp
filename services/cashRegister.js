'use strict'
const CashRegister = require('../models/cashRegister');

//////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////DATA ACCESS METHODS//////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////

async function getCashRegisterById(cashRegisterId) {
  try {
    let cashRegister = await CashRegister.findById(cashRegisterId);
    return cashRegister;
  }
  catch (err) {
    throw new Error(err);
  }
}

module.exports = {
  getCashRegisterById
}