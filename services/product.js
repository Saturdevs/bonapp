'use strict'
const Product = require('../models/product');

//////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////DATA ACCESS METHODS//////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////

async function getProductById(productId) {
  try {
    let product = await Product.findById(productId);
    return product;
  }
  catch (err) {
    throw new Error(err);
  }
}

module.exports = {
  getProductById
}